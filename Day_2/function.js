function add(num1, num2) {
  return num1 + num2;
}

console.log(add(2, 3));

let substract = function (a, b) {
  return a - b;
};

console.log(substract(5, 3));

const multiply = (a, b) => {
  return a * b;
};

console.log(multiply(2, 3));

const divide = (a, b) => a / b;

console.log(divide(10, 5));
